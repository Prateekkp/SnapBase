def show_banner():
    colors = [
        '\033[38;5;33m',   # Blue
        '\033[38;5;45m',   # Cyan
        '\033[38;5;87m',   # Sky blue
        '\033[38;5;135m',  # Purple
        '\033[38;5;170m',  # Medium purple
        '\033[38;5;213m',  # Pink
    ]
    
    BOLD = '\033[1m'
    END = '\033[0m'

    banner = r"""
▀▀▀▀▀▀▀▀▀▀▀▀▀▀▀▀▀▀▀▀▀▀▀▀▀▀▀▀▀▀▀▀▀▀▀▀▀▀▀▀▀▀▀▀▀▀▀▀▀▀▀▀▀▀▀▀▀▀▀▀▀▀▀▀▀▀▀▀▀
 ██████╗███╗   ██╗ █████╗ ██████╗     ██████╗  █████╗ ███████╗███████╗
██╔════╝████╗  ██║██╔══██╗██╔══██╗    ██╔══██╗██╔══██╗██╔════╝██╔════╝
╚█████╗ ██╔██╗ ██║███████║██████╔╝    ██████╔╝███████║███████╗█████╗  
 ╚═══██╗██║╚██╗██║██╔══██║██╔═══╝     ██╔══██╗██╔══██║╚════██║██╔══╝  
██████╔╝██║ ╚████║██║  ██║██║         ██████╔╝██║  ██║███████║███████╗
╚═════╝ ╚═╝  ╚═══╝╚═╝  ╚═╝╚═╝         ╚═════╝ ╚═╝  ╚═╝╚══════╝╚══════╝
▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄▄
"""
    
    lines = banner.strip().split('\n')
    total_lines = len(lines)
    
    print(f"\n{BOLD}")
    for i, line in enumerate(lines):
        color_index = int((i / total_lines) * len(colors))
        color_index = min(color_index, len(colors) - 1)
        print(f"{colors[color_index]}{line}")
    print(END)

if __name__ == "__main__":
    show_banner()
